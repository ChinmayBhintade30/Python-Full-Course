#Print multiplicaton table - using while loop


num = int(input("Enter the number: "))

i = 1
while(i<11):
    print(f"{num} * {i} = {num * i}")
    i+=1

#used i = 1, to initiate and i+=1 to iterate, and used condition while(i<11) so that it print the table from 1 to 10 
#and excludes 11




