import math

print(math.ceil(2.9))
#smallest integer greater than or equal to x

#the output is 3 which means it rounds of the value to 3 which is very close to the 2.9 

print(math.floor(2.9))#2 

# floor -->floor of x is the largest integer less than or equal to x
#it gives 2 , the smallest nearest whole number 
#or we can say the value left to the decimal is considered as round off

